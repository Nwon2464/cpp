int check(string &S, string &query, int t) {
     string target = S.substr(t, sz(query));
     int freq = 0;
     for (int i = 0; i < sz(query); i++) {
          if (query[i] == target[i]) {
               freq++;
          } else {
               break;
          }
     }
     return freq;
}
bool bs(vector<int> &suffix, string &S, string &query_string) {
     // this function was initial thought process, it used for loop to check
     // every string
     int left = 0, right = sz(suffix) - 1;

     while (left <= right) {
          int mid = left + (right - left) / 2;
          int freq = check(S, query_string, suffix[mid]);
          if (freq == sz(query_string)) {
               return 1;
          } else if ((int)query_string[freq] <= (int)S[suffix[mid] + freq]) {
               right = mid - 1;
          } else {
               left = mid + 1;
          }
     }
     return 0;
}
